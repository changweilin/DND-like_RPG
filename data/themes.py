def get_themed_name(key, world_id='dnd5e', category='item'):
    """Return the themed display name for key in world_id.

    Falls back: world_id → 'dnd5e' → raw key.
    category: 'item' | 'monster' | 'spell' | 'skill'
    """
    entry = THEME_NAMES.get(category, {}).get(key)
    if not entry:
        return key
    return entry.get(world_id) or entry.get('dnd5e') or key


def get_all_themed_names(world_id='dnd5e'):
    """Return a flat dict {key: themed_name} for all categories in world_id."""
    result = {}
    for _cat, _entries in THEME_NAMES.items():
        for _key, _variants in _entries.items():
            result[_key] = _variants.get(world_id) or _variants.get('dnd5e') or _key
    return result
